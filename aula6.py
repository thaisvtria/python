# Operadores ariteticos
# Soma
soma = 10 + 5 
print(soma)
# Subtração
subtracao = 10 - 5
print(subtracao)
# Multiplicação
multiplicacao = 10 * 5
print(multiplicacao)
# Divisão
divisao = 10 / 5
print(divisao)
# Divisão inteira
divisao_inteira = 10 // 5
print(divisao_inteira)
# Exponenciação
exponenciacao = 10 ** 2
print(exponenciacao)
# Módulo / resto da divisão 
módulo = 10 % 5
print(módulo)
